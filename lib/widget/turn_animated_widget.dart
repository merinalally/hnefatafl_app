import 'package:flutter/material.dart';

class TurnAnimatedWidget extends StatefulWidget {
  const TurnAnimatedWidget({
    super.key,
    required this.child,
  });

  final Widget child;

  @override
  State<TurnAnimatedWidget> createState() => _PulsingIconState();
}

class _PulsingIconState extends State<TurnAnimatedWidget>
    with SingleTickerProviderStateMixin {

  late AnimationController _controller;
  late Animation<double> _scaleAnimation;

  @override
  void initState() {
    super.initState();

    _controller = AnimationController(
      vsync: this,
      duration: const Duration(milliseconds: 800),
    )..repeat(reverse: true);

    _scaleAnimation = Tween<double>(
      begin: 1.0,
      end: 1.2,
    ).animate(
      CurvedAnimation(
        parent: _controller,
        curve: Curves.easeInOut,
      ),
    );
  }

  @override
  void dispose() {
    _controller.dispose();
    super.dispose();
  }

  @override
  Widget build(BuildContext context) {
    return ScaleTransition(
      scale: _scaleAnimation,
      child: widget.child,
    );
  }
}
